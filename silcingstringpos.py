name = "Adarsh Daniel Kanikaram "
# slicing using start and end index
print(f"Your forename is : {name[0:6]}")
print(f"Your middle name is : {name[7:13]}")
print(f"Your surname is : {name[14:23]}")
# slicing without start/end index 
print(f"Your first name is : {name[:6]}")
print(f"Your surname is : {name[14:]}")
# slicing using negative indexes
print(f"Your first name is : {name[-24:-18]}")

 




